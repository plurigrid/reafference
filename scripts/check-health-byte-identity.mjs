import { readFile } from "node:fs/promises";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

import { buildHealthFrameBytes, frameHashHex } from "../shared/health-frame.mjs";

const scriptDir = dirname(fileURLToPath(import.meta.url));
const repoRoot = resolve(scriptDir, "..");
const builtFramePath = resolve(repoRoot, "build/health/frame.ansi");

const generatedBytes = buildHealthFrameBytes();
const builtBytes = new Uint8Array(await readFile(builtFramePath));

const generatedHash = await frameHashHex(generatedBytes);
const builtHash = await frameHashHex(builtBytes);

const matchesLength = generatedBytes.length === builtBytes.length;
const matchesHash = generatedHash === builtHash;

if (!matchesLength || !matchesHash) {
  process.stderr.write(`byte identity check failed\n`);
  process.stderr.write(`generated bytes=${generatedBytes.length} sha256=${generatedHash}\n`);
  process.stderr.write(`built     bytes=${builtBytes.length} sha256=${builtHash}\n`);
  process.exit(1);
}

process.stdout.write(`byte identity ok :: ${builtBytes.length} bytes :: ${builtHash}\n`);
